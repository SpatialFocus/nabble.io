namespace Nabble.Core.Builder
{
	/// <summary>
	/// An enum representing possible colors of a Badge.
	/// Corresponds to the <see href="http://shields.io/">shields.io</see> color scheme.
	/// </summary>
	public enum BadgeColor
	{
		/// <summary>
		/// A color representing bright green.
		/// </summary>
		BrightGreen,

		/// <summary>
		/// A color representing green.
		/// </summary>
		Green,

		/// <summary>
		/// A color representing yellow.
		/// </summary>
		Yellow,

		/// <summary>
		/// A color representing yellow green.
		/// </summary>
		Yellowgreen,

		/// <summary>
		/// A color representing orange.
		/// </summary>
		Orange,

		/// <summary>
		/// A color representing red.
		/// </summary>
		Red,

		/// <summary>
		/// A color representing blue.
		/// </summary>
		Blue,

		/// <summary>
		/// A color representing grey.
		/// </summary>
		Grey,

		/// <summary>
		/// A color representing gray.
		/// </summary>
		Gray,

		/// <summary>
		/// A color representing light grey.
		/// </summary>
		Lightgrey,

		/// <summary>
		/// A color representing light gray.
		/// </summary>
		Lightgray
	}
}